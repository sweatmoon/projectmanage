// ── DB 기반 동적 공휴일 캐시 ──────────────────────────────────────────────────
// 앱 시작 시 fetchAndCacheHolidays() 를 호출하면 DB 값으로 캐시를 채운다.
// React useSyncExternalStore 패턴으로 캐시 변경 시 구독 컴포넌트가 리렌더됩니다.

let _dynamicCache: ReadonlySet<string> | null = null;
let _dynamicNames: Record<string, string> = {};
let _holidayVersion = 0; // 캐시 변경 버전 카운터
const _listeners = new Set<() => void>(); // 변경 구독자

/** 캐시가 변경됐음을 모든 구독자에게 알림 */
function _notifyListeners() {
  _holidayVersion++;
  _listeners.forEach(fn => fn());
}

/** 구독 함수 (useSyncExternalStore용) */
export function subscribeHolidayCache(cb: () => void): () => void {
  _listeners.add(cb);
  return () => _listeners.delete(cb);
}

/** 현재 버전 스냅샷 반환 (useSyncExternalStore용) */
export function getHolidayVersion(): number {
  return _holidayVersion;
}

/** DB API에서 공휴일을 가져와 캐시에 저장. 앱 초기화 시 1회 호출
 *  DB에 데이터가 있으면 DB만 사용 (하드코딩 무시).
 *  DB가 비어있거나 오류 시에만 하드코딩 폴백 사용.
 */
export async function fetchAndCacheHolidays(): Promise<void> {
  try {
    const res = await fetch('/api/v1/holidays', { credentials: 'include' });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const data = await res.json();
    const list: { date: string; name: string }[] = data?.holidays ?? [];

    if (list.length > 0) {
      // DB에 데이터가 있으면 DB만 사용 (하드코딩 무시)
      const dbSet = new Set<string>();
      const dbNames: Record<string, string> = {};
      for (const h of list) {
        dbSet.add(h.date);
        dbNames[h.date] = h.name;
      }
      _dynamicCache = dbSet;
      _dynamicNames = dbNames;
      console.info(`[holidays] 공휴일 캐시 로드: DB ${list.length}건만 사용 (하드코딩 무시)`);
    } else {
      // DB가 비어있으면 하드코딩 폴백
      _dynamicCache = KOREAN_HOLIDAYS;
      _dynamicNames = { ...HOLIDAY_NAMES };
      console.info(`[holidays] DB 비어있음, 하드코딩 폴백 사용: ${KOREAN_HOLIDAYS.size}건`);
    }
    _notifyListeners(); // ← React에 변경 알림
  } catch (e) {
    console.warn('[holidays] DB 로드 실패, 하드코딩 폴백 사용:', e);
  }
}

/** 현재 활성 공휴일 Set (DB+하드코딩 합산, 로드 전이면 하드코딩만) */
export function getActiveHolidaySet(): ReadonlySet<string> {
  return _dynamicCache ?? KOREAN_HOLIDAYS;
}

/** 현재 활성 공휴일 이름 맵 (DB 캐시 우선, 없으면 하드코딩) */
export function getActiveHolidayNames(): Record<string, string> {
  return Object.keys(_dynamicNames).length > 0 ? _dynamicNames : HOLIDAY_NAMES;
}

/** 동적 캐시 기반 공휴일 여부 */
export function isHolidayDynamic(dateStr: string): boolean {
  return getActiveHolidaySet().has(dateStr);
}

/** 동적 캐시 기반 주말/공휴일 여부 */
export function isNonWorkdayDynamic(year: number, month: number, day: number): boolean {
  const d = new Date(year, month - 1, day);
  if (d.getDay() === 0 || d.getDay() === 6) return true;
  const dateStr = `${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
  return getActiveHolidaySet().has(dateStr);
}

/** 동적 캐시 기반 공휴일 이름 반환 */
export function getHolidayNameDynamic(dateStr: string): string | null {
  return getActiveHolidayNames()[dateStr] ?? null;
}

/** 동적 캐시 기반 영업일 수 계산 */
export function countBusinessDaysDynamic(startStr: string, endStr: string): number {
  if (!startStr || !endStr) return 0;
  const start = new Date(startStr + 'T00:00:00');
  const end = new Date(endStr + 'T00:00:00');
  if (isNaN(start.getTime()) || isNaN(end.getTime()) || start > end) return 0;
  const holidays = getActiveHolidaySet();
  let count = 0;
  const cur = new Date(start);
  while (cur <= end) {
    const dow = cur.getDay();
    const ds = `${cur.getFullYear()}-${String(cur.getMonth()+1).padStart(2,'0')}-${String(cur.getDate()).padStart(2,'0')}`;
    if (dow !== 0 && dow !== 6 && !holidays.has(ds)) count++;
    cur.setDate(cur.getDate() + 1);
  }
  return count;
}

/** 날짜가 공휴일인지 확인 */
export function isHoliday(dateStr: string): boolean {
  return KOREAN_HOLIDAYS.has(dateStr);
}

/** 날짜가 주말이거나 공휴일인지 확인 (영업일이면 false) */
export function isNonWorkday(year: number, month: number, day: number): boolean {
  const d = new Date(year, month - 1, day);
  const dow = d.getDay();
  if (dow === 0 || dow === 6) return true;
  const dateStr = `${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
  return KOREAN_HOLIDAYS.has(dateStr);
}

/** 날짜 문자열(YYYY-MM-DD)로 주말/공휴일 여부 확인 */
export function isNonWorkdayStr(dateStr: string): boolean {
  const d = new Date(dateStr + 'T00:00:00');
  const dow = d.getDay();
  if (dow === 0 || dow === 6) return true;
  return KOREAN_HOLIDAYS.has(dateStr);
}

/** 동적 캐시 기반 주말/공휴일 여부 (string 버전) */
export function isNonWorkdayStrDynamic(dateStr: string): boolean {
  const d = new Date(dateStr + 'T00:00:00');
  const dow = d.getDay();
  if (dow === 0 || dow === 6) return true;
  return getActiveHolidaySet().has(dateStr);
}

/** 두 날짜 사이의 영업일 수 계산 (주말 + 공휴일 제외, 양 끝 포함) */
export function countBusinessDays(startStr: string, endStr: string): number {
  if (!startStr || !endStr) return 0;
  const start = new Date(startStr + 'T00:00:00');
  const end = new Date(endStr + 'T00:00:00');
  if (isNaN(start.getTime()) || isNaN(end.getTime()) || start > end) return 0;
  let count = 0;
  const cur = new Date(start);
  while (cur <= end) {
    const dow = cur.getDay();
    const ds = `${cur.getFullYear()}-${String(cur.getMonth()+1).padStart(2,'0')}-${String(cur.getDate()).padStart(2,'0')}`;
    if (dow !== 0 && dow !== 6 && !KOREAN_HOLIDAYS.has(ds)) count++;
    cur.setDate(cur.getDate() + 1);
  }
  return count;
}


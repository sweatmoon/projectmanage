def is_business_day(d: date) -> bool:
    """주말 및 공휴일 제외 영업일 여부"""
    if d.weekday() >= 5:  # 토(5), 일(6)
        return False
    return d.strftime("%Y-%m-%d") not in KOREAN_HOLIDAYS


def count_business_days(start: date, end: date) -> int:
    """두 날짜 사이의 영업일 수 (주말 + 공휴일 제외, 양 끝 포함)"""
    if not start or not end or start > end:
        return 0
    count = 0
    cur = start
    while cur <= end:
        if is_business_day(cur):
            count += 1
        cur += timedelta(days=1)
    return count


def get_consecutive_business_days(start: date, end: date, num_days: int) -> List[date]:
    """start~end 범위에서 공휴일/주말 제외 연속 영업일 num_days개 반환"""
    result: List[date] = []
    cur = start
    while len(result) < num_days and cur <= end:
        if is_business_day(cur):
            result.append(cur)
        cur += timedelta(days=1)
    return result


